@남상도 CREATE TABLE BOARD (
    BOARD_NUM INT PRIMARY KEY,      -- 게시글의 고유 식별자 (기본키)
    BOARD_TITLE VARCHAR(100) NOT NULL,    -- 게시글 제목
    BOARD_CONTENT VARCHAR(1000),          -- 게시글 내용 (NULL 허용)
    BOARD_CNT INT DEFAULT 0,           -- 조회수 (기본값 0)
    BOARD_LOCATION VARCHAR(100) NOT NULL, -- 게시글 지역
    BOARD_WRITER_ID VARCHAR(100),   -- 글 작성자의 FK (외래키)
    -- 외래키 제약조건 설정
    CONSTRAINT FK_BOARD_MEMBER_ID 
    FOREIGN KEY (BOARD_MEMBER_ID)
    REFERENCES MEMBER(ID)           -- BOARD_MEMBER_ID가 MEMBER 테이블의 ID를 참조
    ON DELETE CASCADE               -- MEMBER가 삭제되면 BOARD도 삭제
);

