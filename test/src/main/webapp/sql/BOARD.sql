-- 최신글 5개 검색 SelectAll(ALLROWNUM)
SELECT 
    BOARD_NUM,
    BOARD_TITLE,
    BOARD_CONTENT,
    BOARD_CNT,
    BOARD_LOCATION,
    BOARD_WRITER_ID
FROM (
    SELECT 
        B.BOARD_NUM, 
        B.BOARD_TITLE, 
        B.BOARD_CONTENT, 
        B.BOARD_CNT, 
        B.BOARD_LOCATION, 
        B.BOARD_WRITER_ID
    FROM 
        BOARD B
    JOIN 
        MEMBER M
    ON 
        B.BOARD_WRITER_ID = M.MEMBER_ID
    ORDER BY 
        B.BOARD_NUM DESC
)
WHERE ROWNUM <= 5;  
    
